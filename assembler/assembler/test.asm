	LJMP	START
	ORG	100H
START:
	CLR RS1
	CLR RS0
	MOV R7, #0
	CPL RS0
	MOV R7, #1
	CPL RS1
	MOV R7, #3
	CPL RS0
	MOV R7, #2
	
	LCALL LCD_INIT
	LCALL LCD_CLR

LOOP:
	CLR RS1
	CLR RS0
	MOV A, R7
	LCALL LCD_CLR
	LCALL WRITE_HEX
	MOV A, #10
	LCALL DELAY_100MS
	
	CPL RS0
	MOV A, R7
	LCALL LCD_CLR
	LCALL WRITE_HEX
	MOV A, #10
	LCALL DELAY_100MS
	
	CPL RS0
	CPL RS1
	MOV A, R7
	LCALL LCD_CLR
	LCALL WRITE_HEX
	MOV A, #10
	LCALL DELAY_100MS
	
	CPL RS0
	MOV A, R7
	LCALL LCD_CLR
	LCALL WRITE_HEX
	MOV A, #10
	LCALL DELAY_100MS

	SJMP LOOP
	
STOP:
	SJMP $
	NOP
